export default memo(function SAndLSVGPlates({
  sAndLStates,
  dispatch,
  pageName,
  diceValue,
  specialIndexes,
}) {
  const [targetPlate, setTargetPlate] = useState(sAndLStates.activePlate);
  const [hoveredIndex, setHoveredIndex] = useState(null);

  useEffect(() => {
    dispatch({
      type: "ACTIVE_TIME",
      payload: 150,
    });
  }, [dispatch]);

  let interval;

  // Пошаговая анимация
  useEffect(() => {
    if (targetPlate !== sAndLStates.activePlate) {
      interval = setTimeout(() => {
        dispatch({
          type: "ACTIVE_PLATE",
          payload:
            sAndLStates.activePlate < targetPlate
              ? sAndLStates.activePlate + 1
              : sAndLStates.activePlate - 1,
        });

        if (
          sAndLStates.activePlate === targetPlate - 1 ||
          sAndLStates.activePlate === targetPlate + 1
        ) {
          clearInterval(interval);
          dispatch({
            type: "ANIM_IN_PROG",
            payload: false,
          });
        }
      }, sAndLStates.activeTime - 50);
    }

    return () => clearInterval(interval);
  }, [sAndLStates.activePlate, targetPlate]);

  // Работа с порталами
  useEffect(() => {
    const validIndexes = specialIndexes.slice(0, -1); // Игнорируем последнюю строку с "chest"
    const index = validIndexes.findIndex(
      (item) => sAndLStates.activePlate === item[0][0]
    );
    if (!sAndLStates.animInProgress && index !== -1) {
      const targetPlate = validIndexes[index][0][1];
      setTimeout(() => {
        dispatch({ type: "ANIM_PORTAL", payload: true });
      }, 400);
      setTimeout(() => {
        setTargetPlate(targetPlate);
        dispatch({ type: "ACTIVE_PLATE", payload: targetPlate });
        dispatch({ type: "ANIM_PORTAL", payload: false });
      }, 800);
    }
  }, [specialIndexes, sAndLStates.activePlate, sAndLStates.animInProgress]);

  // Вставляем значение кубиков
  useEffect(() => {
    setTimeout(() => {
      setTargetPlate(targetPlate + diceValue);
    }, 800);
  }, [diceValue]);

  // Обработчик клика по плашке
  const handleClick = (index) => {
    if (!sAndLStates.animInProgress && index !== sAndLStates.activePlate) {
      dispatch({
        type: "ANIM_IN_PROG",
        payload: true,
      });
      setTargetPlate(index);
    }
  };

  // Обработчик наведения на плашку если она равна индексу chest
  const handleMouseOver = (index) => {
    const isChest = specialIndexes.find(
      (item) => item[1] === "chest" && item[0].includes(index)
    );
    setHoveredIndex(isChest ? index + 1 : null);
  };

  // Обработчик ухода мыши с плашки
  const handleMouseLeave = () => {
    setHoveredIndex(null);
  };

  return (
    <svg viewBox="-10 -10 1109.86 530.09">
      {paths.map((path, index) => (
        <path
          key={index}
          d={path}
          onClick={() => handleClick(index)}
          onMouseOver={() => handleMouseOver(index)}
          onMouseLeave={() => handleMouseLeave()}
        />
      ))}

      {hoveredIndex !== null && (
        <ItemNotiz
          pageName={pageName}
          positiom={hoveredIndex && pathsPositions[`path${hoveredIndex}`]}
          items={[
            ["img/sAndL/ringIcn.png", 1],
            ["img/sAndL/necklaceIcn.png", 6],
            ["img/sAndL/jewelryBagIcn.png", 12],
            ["img/sAndL/heroKeyIcn.png", 88],
          ]}
          onMouseOver={() => setHoveredIndex(hoveredIndex)}
          onMouseLeave={() => {
            setHoveredIndex(null);
          }}
        />
      )}
    </svg>
  );
});
